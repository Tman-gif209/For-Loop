
#For this task I am required to dtermine if a year is a leap year by multiplying it by 4.

year = int(input("Enter the year that you would like to start with: "))
check = int(input("How many years do you want to check: "))


for x in range( year, check + year):
    if x % 4 == int():
        print(f"{x} is a leap year. ")

    else: print(f"{x} is not a leap year.")
